//To demonstrate the working of bitwise operators
class BitwiseOperators{

public static void main(String[]args){
int a=5;
int b=6;
int and=a&b; 
int or=a|b;
int xor=a^b;
System.out.println("The value of a & b is: " +and);
System.out.println("The value of a | b is: " +or);
System.out.println("The value of a ^ b is: "+xor);
}
}