//To demonstrate the Logical Operators
class LogicalOperators{
public static void main (String[]args){
int x=8, y=6, z=7;
System.out.println("x>y & y>z=" +(x>y & y>z));
System.out.println("x>y | y>z=" +(x>y | y>z));
System.out.println("x>y ^ y>z=" +(x>y  ^ y>z));
System.out.println("x>y && y>z=" +(x>y && y>z));
System.out.println("x>y || y>z=" +(x>y || y>z));
System.out.println("!(x>y)=" +(!(x>y)));  
}   
}
